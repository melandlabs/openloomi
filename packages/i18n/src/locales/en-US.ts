function deriveUnderstandingCopy(input: unknown): unknown {
  if (Array.isArray(input)) {
    return input.map((item) => deriveUnderstandingCopy(item));
  }
  if (input && typeof input === "object") {
    return Object.fromEntries(
      Object.entries(input as Record<string, unknown>).map(([key, value]) => [
        key,
        deriveUnderstandingCopy(value),
      ]),
    );
  }
  if (typeof input === "string") {
    return input
      .replace(/\bSummaries\b/g, "Understandings")
      .replace(/\bsummaries\b/g, "understandings")
      .replace(/\bSummary\b/g, "Understanding")
      .replace(/\bsummary\b/g, "understanding");
  }
  return input;
}

